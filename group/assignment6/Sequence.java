package assignment_6;

public class Sequence {
	private int car;
	private char from;
	private char to;

	public Sequence(int car, char from, char to) {
		this.car = car;
		this.from = from;
		this.to = to;
	}

	public int getCar() {
		return car;
	}

	public char getFrom() {
		return from;
	}

	public char getTo() {
		return to;
	}
}
